You are not you--you have no body, no blood, no bones, you are but a thought. I myself have no existence; I am but a dream--your dream, a creature of your imagination. 
In a moment you will have realized this, then you will banish me from your visions and I shall dissolve into the nothingness out of which you made me
In a little while you will be alone in shoreless space, to wander its limitless solitudes without friend or comrade forever—for you will remain a thought, the only existent thought, and by your nature inextinguishable, indestructible. But I, your poor servant, have revealed you to yourself and set you free. Dream other dreams, and better!
Strange! that you should not have suspected years ago—centuries, ages, eons, ago!—for you have existed, companionless, through all the eternities.
Strange, indeed, that you should not have suspected that your universe and its contents were only dreams, visions, fiction! 
Strange, because they are so frankly and hysterically insane—like all dreams: a God who could make good children as easily as bad, yet preferred to make bad ones; who could have made every one of them happy, yet never made a single happy one; who made them prize their bitter life, yet stingily cut it short; who gave his angels eternal happiness unearned, yet required his other children to earn it; who gave his angels painless lives, yet cursed his other children with biting miseries and maladies of mind and body; who mouths justice and invented hell—mouths mercy and invented hell—mouths Golden Rules, and forgiveness multiplied by seventy times seven, and invented hell; who mouths morals to other people and has none himself; who frowns upon crimes, yet commits them all; who created man without invitation, then tries to shuffle the responsibility for man's acts upon man, instead of honorably placing it where it belongs, upon himself; and finally, with altogether divine obtuseness, invites a poor, abused slave 
